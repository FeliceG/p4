  <h1>Eligibility</h1>

	   <p>The research sessions are held to exchange information, ideas, and latest research results in the field of coaching. Papers and posters need to be relevant to the following areas:</p>
    	   <ul>
        		<li>Leadership Coaching</li>
        		<li>Health and Wellness Coaching</li>
        		<li>Positive Psychology</li>
        		<li>Life Coaching</li>
        		<li>Other relevant fields</li>
    	  </ul>
  	<p>Papers and posters can describe original empirical research (quantitative, qualitative or mixed-methods research), meta-analyses, and theoretical discussions. </p>

  	<p>Each author can submit up to two different submissions of paper and/or poster.</p>

      	<ul>
      		<li>Primary authors must be investigators who are currently affiliated with academic institutions, are doctoral level professionals (PhD, MD, EdD) in other institutions, or are graduate students and other professionals with doctoral level mentors.</li>
      		<li>Empirical research should be completed or well under way rather than at the beginning stages. It must include analyses and a summary of findings. Stating "the data will be analyzed" or "findings will be presented/discussed" is not sufficient research to submit for either the poster or paper presentation.</li>
      		<li>The submission cannot have been presented at any previous Institute of Coaching conference.</li>
      		<li>The submission cannot have been previously published.</li>
      		<li>The study must have followed the standards of ethics in research with human beings and obtained appropriate ethical approval if it is an empirical study.</li>
      		<li>The Scientific Advisory Council of the Institute of Coaching will review all paper and poster submissions and select those to be presented at the conference as well as the winners of the Best Poster Competition and Honorary Mention winners.</li>
      		<li class="bold">Submissions that promote commercial interests or that feature proprietary materials will not be accepted.</li>
      		<li>Presenting authors of all accepted papers and posters must register and pay the conference registration fee.</li>
      	</ul>

	  <h2>Poster Competition</h2>

    <p class="bold">All those invited to submit a poster will be entered into the Best Poster competition.</p>

	  <p>The Primary Author of the Best Poster award receives:</p>

    	<ol>
    		<li>One-year complementary Affiliate Membership in the ICPA.</li>
    		<li>Up to $1000 travel and accommodation reimbursement for the current conference. (Travel must meet the Partners guidelines for reimbursement.)</li>
    		<li>Complementary registration to next year's Coaching Conference.</li>
    		<li>A commemorative award.</li>
    	</ol>

