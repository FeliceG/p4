<h3 class="bold" class="line">Paper and Poster Submission Guidelines</h3>

<ul>
<li>Papers and posters can describe original empirical research, meta-analyses, and theoretical discussions.</li>
<li><span class="bold">The deadline</span> for the receipt of all applications online is Tuesday, June 30, 2016 by 11:59 PM EDT. Decisions will be announced by  Wednesday, July 15, 2016.</li>
<li>Author(s) details can include up to 2 authors. The first author is the presenting author.</li>
<li><span class="bold">Title</span> - up to 30 words</li>
<li><span class="bold">Abstracts</span> for publication in the Conference program are up to 150 words</li>
</ul>

<p>The following fields are up to 100 words each:</p>

<ul>
<li><span class="bold">Background and Objectives:</span> include the objectives of the study, main research questions, hypotheses, or theory being tested.</li>
<li><span class="bold">Design and Methods:</span> include design, participants, measures, data collection and analysis.</li>
<li><span class="bold">Findings:</span> Quantitative studies should include a description of the results; qualitative studies should include a description of the main findings such as the themes or categories generated. Note: it is not acceptable to state "findings will be presented."</li>
<li><span class="bold">Discussion</span>: Discussion and conclusions regarding the implications and possible contribution to Coaching. Please do not include a list of references at the end.</li>
</ul>


<h4>To submit a poster or paper, please <a href="/register">register</a> to create an account. Once registered, you will be redirected to the submission form.</h4>


<p class="bold">The criteria for selecting and awarding posters include:</p>

<ul>
<li>Clarity of research aims and rationale;</li>
<li>Adequacy of study design and methodology;</li>
<li>Visual presentation;</li>
<li>Likelihood to influence coaching practice and/or research;</li>
<li>Potential to publish.</li>
</ul>

<p class="bold">Additional information for accepted posters:</p>
<ul>
<li>All invited to submit a full poster will be entered into a competition for Best Poster: For more details see the Conference website.</li>
<li>Accepted poster presenters must submit the final PDF poster by Friday, August 15, 2016 11:59 PM EDT.</li>
<li>Be concise with written material. Save elaborate points for discussion or interaction with attendees during poster sessions.</li>
<li>Use graphs, color, charts, images, and/or tables to show results and create a visually interesting poster.</li>
<li>Posters can be no larger than 72 inches wide by 48 inches tall.</li>
<li>Print your final poster and bring to the Attendee Assistance Table by 8:00am on Friday, September 25.</li>
<li>Posters will be mounted on poster stands by the hotel staff and available for attendees to view during conference meeting breaks and the Poster Session, scheduled the evening of Friday, September 16.</li>
</ul>

